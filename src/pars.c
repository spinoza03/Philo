#include "philo.h"

int	ft_isdigit(int c)
{
	return (c >= '0' && c <= '9');
}

int	is_valid_integer(char *str)
{
	if (!str || !*str)
		return (0);
	if (*str == '-' || *str == '+')
		str++;
	if (!*str)
		return (0);
	while (*str)
	{
		if (!ft_isdigit(*str))
			return (0);
		str++;
	}
	return (1);
}

int	pars_args(char *str)
{
	if (!is_valid_integer(str))
		return (0);
	if (!ft_atoi(str)) // what if ./philo 0 0 0 0
		return (0);
	return (1);
}
